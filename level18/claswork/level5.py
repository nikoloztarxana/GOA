#მომხმარებელს შემოატანინეთ სამი რიცხვი start,end,step და range ფუნქციის გამოყენებით გამოიტანეთ start იდან end ის ჩათვლით გამოიტანეთ ყველა რიცხვი
# მომხმარებლისგან რიცხვების მიღება
start = int(input("შეიყვანეთ საწყისი რიცხვი: "))
end = int(input("შეიყვანეთ საბოლოო რიცხვი: "))

# რიცხვების გამოტანა range ფუნქციის გამოყენებით
for i in range(start,end,):
    print(i)